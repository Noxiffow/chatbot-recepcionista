async def update_or_create_conversation(
    db: aiosqlite.Connection,
    from_number: str,
) -> int:
    """Update existing conversation or create a new one. Returns conversation id.
    Fase 7: Checks last_activity — if > 24h, starts fresh conversation.
    """
    now = datetime.now(timezone.utc)
    now_iso = now.isoformat()

    # Try to find active conversation
    cursor = await db.execute(
        "SELECT id, message_count, last_activity FROM conversations "
        "WHERE from_number = ? AND status = 'active'",
        (from_number,),
    )
    row = await cursor.fetchone()

    if row:
        conv_id, count, last_activity = row["id"], row["message_count"], row["last_activity"]

        # Fase 7: If last activity > 24h, close old conversation and start new
        if last_activity:
            try:
                last_dt = datetime.fromisoformat(last_activity)
                if (now - last_dt).total_seconds() > 86400:  # 24h
                    await db.execute(
                        "UPDATE conversations SET status = 'closed' WHERE id = ?",
                        (conv_id,),
                    )
                    await db.commit()
                    logger.info(f"🕐 Conversation {conv_id} auto-closed (>24h inactive)")
                    # Fall through to create new conversation
                    row = None
            except (ValueError, TypeError):
                pass  # Invalid timestamp, just update
        else:
            # No last_activity set yet, treat as active
            pass

    if row:
        conv_id = row["id"]
        count = row["message_count"]
        await db.execute(
            "UPDATE conversations SET last_message_at = ?, last_activity = ?, message_count = ? WHERE id = ?",
            (now_iso, now_iso, count + 1, conv_id),
        )
        await db.commit()
        return conv_id
    else:
        cursor = await db.execute(
            "INSERT INTO conversations (from_number, started_at, last_message_at, last_activity) "
            "VALUES (?, ?, ?, ?)",
            (from_number, now_iso, now_iso, now_iso),
        )
        await db.commit()
        return cursor.lastrowid


async def get_conversation_history(
    db: aiosqlite.Connection,
    from_number: str,
    limit: int = 10,
) -> list[dict]:
    """Get recent conversation history for context."""
    cursor = await db.execute(
        """
        SELECT text, ai_response, direction, received_at
        FROM messages
        WHERE from_number = ?
        ORDER BY received_at DESC
        LIMIT ?
        """,
        (from_number, limit),
    )
    rows = await cursor.fetchall()
    return [dict(row) for row in reversed(rows)]


