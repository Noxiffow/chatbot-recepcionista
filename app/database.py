async def get_available_slots(
    db: aiosqlite.Connection, date_str: str, duration_min: int
) -> list[dict]:
    """Find consecutive free slots that can accommodate a service of duration_min.
    Returns a list of available start times, each with enough consecutive free slots.
    """
    # Get all free slots for the day
    cursor = await db.execute(
        "SELECT time FROM schedule WHERE date = ? AND status = 'free' ORDER BY time",
        (date_str,),
    )
    free_times = [row[0] for row in await cursor.fetchall()]

    if not free_times:
        return []

    # Convert to minutes for easier math
    free_minutes = sorted([_time_to_minutes(t) for t in free_times])
    slots_needed = (duration_min + SLOT_DURATION_MIN - 1) // SLOT_DURATION_MIN

    available_starts = []

    for i, start_min in enumerate(free_minutes):
        # Check if we have enough consecutive slots starting here
        if i + slots_needed > len(free_minutes):
            break

        consecutive = True
        for j in range(1, slots_needed):
            expected = free_minutes[i] + j * SLOT_DURATION_MIN
            if free_minutes[i + j] != expected:
                consecutive = False
                break

        if consecutive:
            end_min = start_min + duration_min
            available_starts.append({
                "date": date_str,
                "start_time": _minutes_to_time(start_min),
                "end_time": _minutes_to_time(end_min),
                "slots_count": slots_needed,
            })

    return available_starts


async def book_appointment(
    db: aiosqlite.Connection,
    service_id: int,
    client_name: str,
    client_phone: str,
    date_str: str,
    start_time: str,
) -> dict | None:
    """Book an appointment: create appointment record + block schedule slots.
    Returns the appointment dict on success, None if slots are not available.
    """
    # Get service duration
    cursor = await db.execute(
        "SELECT id, name, duration_min, price FROM services WHERE id = ?",
        (service_id,),
    )
    service = await cursor.fetchone()
    if not service:
        logger.error(f"❌ Service {service_id} not found")
        return None

    duration = service["duration_min"]
    end_time = _slot_end_time(start_time, duration)
    slots_needed = (duration + SLOT_DURATION_MIN - 1) // SLOT_DURATION_MIN

    # Verify all needed slots are free
    start_min = _time_to_minutes(start_time)
    slot_times = []
    for i in range(slots_needed):
        slot_min = start_min + i * SLOT_DURATION_MIN
        slot_times.append(_minutes_to_time(slot_min))

    for t in slot_times:
        cursor = await db.execute(
            "SELECT status FROM schedule WHERE date = ? AND time = ?",
            (date_str, t),
        )
        row = await cursor.fetchone()
        if not row or row["status"] != "free":
            logger.warning(f"⚠️ Slot {date_str} {t} is not free — cannot book")
            return None

    # Block all slots
    for t in slot_times:
        await db.execute(
            "UPDATE schedule SET status = 'booked' WHERE date = ? AND time = ?",
            (date_str, t),
        )

    # Create appointment
    now = datetime.now(timezone.utc).isoformat()
    cursor = await db.execute(
        """INSERT INTO appointments
           (service_id, client_name, client_phone, date, start_time, end_time, status, created_at)
           VALUES (?, ?, ?, ?, ?, ?, 'booked', ?)""",
        (service_id, client_name, client_phone, date_str, start_time, end_time, now),
    )
    await db.commit()
    appointment_id = cursor.lastrowid

    logger.info(
        f"📅 Appointment #{appointment_id} booked: {service['name']} for {client_name} "
        f"on {date_str} {start_time}-{end_time}"
    )

    return {
        "id": appointment_id,
        "service_id": service_id,
        "service_name": service["name"],
        "service_price": service["price"],
        "client_name": client_name,
        "client_phone": client_phone,
        "date": date_str,
        "start_time": start_time,
        "end_time": end_time,
        "status": "booked",
    }


async def cancel_appointment(db: aiosqlite.Connection, appointment_id: int) -> bool:
    """Cancel an appointment and free its schedule slots.
    Returns True on success, False if not found.
    """
    cursor = await db.execute(
        "SELECT id, date, start_time, service_id FROM appointments WHERE id = ?",
        (appointment_id,),
    )
    appt = await cursor.fetchone()
    if not appt:
        logger.warning(f"⚠️ Appointment #{appointment_id} not found")
        return False

    # Get service duration
    cursor = await db.execute(
        "SELECT duration_min FROM services WHERE id = ?", (appt["service_id"],)
    )
    service = await cursor.fetchone()
    duration = service["duration_min"] if service else 30

    slots_needed = (duration + SLOT_DURATION_MIN - 1) // SLOT_DURATION_MIN
    start_min = _time_to_minutes(appt["start_time"])

    # Free all slots
    for i in range(slots_needed):
        slot_min = start_min + i * SLOT_DURATION_MIN
        t = _minutes_to_time(slot_min)
        await db.execute(
            "UPDATE schedule SET status = 'free' WHERE date = ? AND time = ?",
            (appt["date"], t),
        )

    # Update appointment status
    await db.execute(
        "UPDATE appointments SET status = 'cancelled' WHERE id = ?",
        (appointment_id,),
    )
    await db.commit()
    logger.info(f"📅 Appointment #{appointment_id} cancelled — slots freed")
    return True


async def get_appointments_for_date(
    db: aiosqlite.Connection, date_str: str
) -> list[dict]:
    """Get all booked appointments for a given date."""
    cursor = await db.execute(
        """SELECT a.id, a.service_id, s.name as service_name, a.client_name,
                  a.client_phone, a.date, a.start_time, a.end_time, a.status
           FROM appointments a
           JOIN services s ON a.service_id = s.id
           WHERE a.date = ? AND a.status = 'booked'
           ORDER BY a.start_time""",
        (date_str,),
    )
    rows = await cursor.fetchall()
    return [dict(row) for row in rows]


async def get_available_dates(
    db: aiosqlite.Connection, duration_min: int, limit: int = 7
) -> list[dict]:
    """Find upcoming dates that have at least one available slot for the given duration.
    Returns dates with available slots.
    """
    today = date.today()
    results = []

    for day_offset in range(30):  # search up to 30 days ahead
        d = today + timedelta(days=day_offset)
        date_str = d.isoformat()
        weekday = d.weekday()
        if not BUSINESS_HOURS.get(weekday):
            continue  # closed day

        slots = await get_available_slots(db, date_str, duration_min)
        if slots:
            results.append({
                "date": date_str,
                "weekday": d.strftime("%A"),
                "available_slots_count": len(slots),
                "first_available": slots[0]["start_time"] if slots else None,
            })

        if len(results) >= limit:
            break

    return results
