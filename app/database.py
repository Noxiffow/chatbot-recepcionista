# Business hours configuration
BUSINESS_HOURS = {
    0: [(9, 14), (16, 20)],  # Monday: 9:00-14:00, 16:00-20:00
    1: [(9, 14), (16, 20)],  # Tuesday
    2: [(9, 14), (16, 20)],  # Wednesday
    3: [(9, 14), (16, 20)],  # Thursday
    4: [(9, 14), (16, 20)],  # Friday
    5: [(9, 14)],            # Saturday: 9:00-14:00
    6: [],                   # Sunday: closed
}
SLOT_DURATION_MIN = 30


def _time_to_minutes(t: str) -> int:
    """Convert HH:MM string to minutes since midnight."""
    h, m = t.split(":")
    return int(h) * 60 + int(m)


def _minutes_to_time(m: int) -> str:
    """Convert minutes since midnight to HH:MM string."""
    return f"{m // 60:02d}:{m % 60:02d}"


def _slot_end_time(start_time: str, duration_min: int) -> str:
    """Calculate end time from a start time and duration."""
    total = _time_to_minutes(start_time) + duration_min
    return _minutes_to_time(total)


async def generate_slots(db: aiosqlite.Connection, days: int = 30) -> int:
    """Generate time slots for the next N days. Only fills empty slots.
    Returns the number of slots created.
    """
    today = date.today()
    count = 0

    for day_offset in range(days):
        d = today + timedelta(days=day_offset)
        date_str = d.isoformat()
        weekday = d.weekday()  # 0=Monday, 6=Sunday

        slots_for_day = BUSINESS_HOURS.get(weekday, [])
        if not slots_for_day:
            continue  # Sunday or closed day

        # Check if slots already exist for this day
        cursor = await db.execute(
            "SELECT COUNT(*) FROM schedule WHERE date = ?", (date_str,)
        )
        existing = (await cursor.fetchone())[0]
        if existing > 0:
            continue  # Slots already generated for this day

        for (start_h, end_h) in slots_for_day:
            current = start_h * 60
            end = end_h * 60
            while current + SLOT_DURATION_MIN <= end:
                time_str = _minutes_to_time(current)
                await db.execute(
                    "INSERT OR IGNORE INTO schedule (date, time, status) VALUES (?, ?, 'free')",
                    (date_str, time_str),
                )
                count += 1
                current += SLOT_DURATION_MIN

    if count > 0:
        await db.commit()
        logger.info(f"📅 Generated {count} new schedule slots for {days} days")
    else:
        logger.info(f"📅 No new slots needed — slots already exist for the next {days} days")

    return count


