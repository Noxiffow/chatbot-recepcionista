async def search_services(db: aiosqlite.Connection, query: str) -> list[dict]:
    """Search services by name with individual word matching.
    Each word in the query is searched separately with LIKE.
    """
    if not query or not query.strip():
        # Return all services
        cursor = await db.execute(
            """SELECT id, name, category, price, duration_min, description
               FROM services ORDER BY price ASC"""
        )
        rows = await cursor.fetchall()
        return [dict(row) for row in rows]

    # Build individual word searches
    words = query.strip().split()
    conditions = []
    params = []
    for word in words:
        if len(word) >= 2:  # Skip single-character words
            conditions.append("(name LIKE ? OR category LIKE ? OR description LIKE ?)")
            params.extend([f"%{word}%", f"%{word}%", f"%{word}%"])

    if not conditions:
        return []

    where_clause = " AND ".join(conditions)
    cursor = await db.execute(
        f"""SELECT id, name, category, price, duration_min, description
            FROM services
            WHERE {where_clause}
            ORDER BY price ASC
            LIMIT 15""",
        params,
    )
    rows = await cursor.fetchall()
    return [dict(row) for row in rows]


