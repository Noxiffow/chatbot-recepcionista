async def get_conversation(db: aiosqlite.Connection, from_number: str) -> dict | None:
    """Get the active conversation for a phone number with state and context."""
    cursor = await db.execute(
        "SELECT * FROM conversations WHERE from_number = ? AND status = 'active' "
        "ORDER BY last_message_at DESC LIMIT 1",
        (from_number,),
    )
    row = await cursor.fetchone()
    if not row:
        return None
    conv = dict(row)
    # Parse context_json
    import json as _json
    try:
        conv["context"] = _json.loads(conv.get("context_json", "{}"))
    except (json.JSONDecodeError, TypeError):
        conv["context"] = {}
    return conv


