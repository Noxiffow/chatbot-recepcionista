# ── SQL schema ──────────────────────────────────────────────
SCHEMA = """
CREATE TABLE IF NOT EXISTS messages (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    wa_message_id TEXT UNIQUE,           -- wamid from WhatsApp
    from_number TEXT NOT NULL,           -- sender phone number
    to_number TEXT,                      -- recipient (business number)
    text TEXT,                           -- message body
    media_type TEXT,                     -- text, image, audio, etc.
    media_url TEXT,                      -- URL to media if present
    timestamp_wa TEXT,                   -- original WhatsApp timestamp
    received_at TEXT NOT NULL DEFAULT (datetime('now')),
    direction TEXT NOT NULL DEFAULT 'incoming',  -- incoming / outgoing
    status TEXT DEFAULT 'received',      -- received, processed, responded, failed
    ai_response TEXT,                    -- response generated by Groq
    ai_model TEXT,                       -- which model generated the response
    ai_tokens_used INTEGER,              -- token usage
    ai_latency_ms INTEGER,              -- latency in ms
    error TEXT,                          -- error message if any
    raw_payload TEXT                     -- full JSON payload from Meta
);

CREATE TABLE IF NOT EXISTS conversations (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    from_number TEXT NOT NULL,
    started_at TEXT NOT NULL DEFAULT (datetime('now')),
    last_message_at TEXT NOT NULL DEFAULT (datetime('now')),
    message_count INTEGER DEFAULT 1,
    status TEXT DEFAULT 'active',        -- active, closed, handed_off
    state TEXT DEFAULT 'IDLE',           -- Fase 7: IDLE, ASKING_PRICE, BOOKING, AWAITING_CONFIRMATION, CONFIRMED, COMPLETED, COMPLAINT, HANDOFF
    context_json TEXT DEFAULT '{}',      -- Fase 7: persisted entities (last_service_mentioned, last_date_mentioned, client_name, etc.)
    last_activity TEXT,                  -- Fase 7: last activity timestamp for 24h timeout
    notes TEXT
);

-- Fase 5B: Availability system
CREATE TABLE IF NOT EXISTS services (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    category TEXT NOT NULL,
    price REAL NOT NULL,
    duration_min INTEGER NOT NULL,
    description TEXT
);

CREATE TABLE IF NOT EXISTS schedule (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    date TEXT NOT NULL,                  -- YYYY-MM-DD
    time TEXT NOT NULL,                  -- HH:MM
    status TEXT NOT NULL DEFAULT 'free', -- free, booked, blocked
    UNIQUE(date, time)
);

CREATE TABLE IF NOT EXISTS appointments (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    service_id INTEGER NOT NULL REFERENCES services(id),
    client_name TEXT NOT NULL DEFAULT '',
    client_phone TEXT NOT NULL,
    date TEXT NOT NULL,                  -- YYYY-MM-DD
    start_time TEXT NOT NULL,            -- HH:MM
    end_time TEXT NOT NULL,              -- HH:MM
    status TEXT NOT NULL DEFAULT 'booked',  -- booked, cancelled, completed
    created_at TEXT NOT NULL DEFAULT (datetime('now'))
);

CREATE INDEX IF NOT EXISTS idx_messages_from ON messages(from_number);
CREATE INDEX IF NOT EXISTS idx_messages_timestamp ON messages(received_at);
CREATE INDEX IF NOT EXISTS idx_messages_wa_id ON messages(wa_message_id);
CREATE INDEX IF NOT EXISTS idx_conversations_from ON conversations(from_number);
CREATE INDEX IF NOT EXISTS idx_services_category ON services(category);
CREATE INDEX IF NOT EXISTS idx_services_name ON services(name);
CREATE INDEX IF NOT EXISTS idx_schedule_date ON schedule(date);
CREATE INDEX IF NOT EXISTS idx_schedule_status ON schedule(status);
CREATE INDEX IF NOT EXISTS idx_appointments_date ON appointments(date);
CREATE INDEX IF NOT EXISTS idx_appointments_phone ON appointments(client_phone);
"""


async def init_db() -> aiosqlite.Connection:
    """Initialize database: create directory and tables."""
    DATA_DIR.mkdir(parents=True, exist_ok=True)

    db = await aiosqlite.connect(config.database_path)
    db.row_factory = aiosqlite.Row
    await db.executescript(SCHEMA)
    await db.commit()

    # Fase 7: Run migrations for new columns
    await _migrate_conversations_table(db)

    logger.info(f"✅ Database initialized at {config.database_path}")
    return db


async def _migrate_conversations_table(db: aiosqlite.Connection) -> None:
    """Fase 7: Add new columns if they don't exist (SQLite doesn't support ALTER COLUMN well)."""
    try:
        await db.execute("ALTER TABLE conversations ADD COLUMN state TEXT DEFAULT 'IDLE'")
        logger.info("📦 Migration: added conversations.state column")
    except Exception:
        pass  # Column already exists

    try:
        await db.execute("ALTER TABLE conversations ADD COLUMN context_json TEXT DEFAULT '{}'")
        logger.info("📦 Migration: added conversations.context_json column")
    except Exception:
        pass

    try:
        await db.execute("ALTER TABLE conversations ADD COLUMN last_activity TEXT")
        logger.info("📦 Migration: added conversations.last_activity column")
    except Exception:
        pass

    await db.commit()


async def save_incoming_message(
    db: aiosqlite.Connection,
    wa_message_id: str,
    from_number: str,
    text: str | None,
    to_number: str | None = None,
    media_type: str = "text",
    media_url: str | None = None,
    timestamp_wa: str | None = None,
    raw_payload: dict | None = None,
) -> int:
    """Save an incoming WhatsApp message. Returns the row id."""
    now = datetime.now(timezone.utc).isoformat()
    raw_json = json.dumps(raw_payload) if raw_payload else None

    cursor = await db.execute(
        """
        INSERT INTO messages
            (wa_message_id, from_number, to_number, text, media_type, media_url,
             timestamp_wa, received_at, direction, status, raw_payload)
        VALUES (?, ?, ?, ?, ?, ?, ?, ?, 'incoming', 'received', ?)
        ON CONFLICT(wa_message_id) DO NOTHING
        """,
        (wa_message_id, from_number, to_number, text, media_type, media_url,
         timestamp_wa, now, raw_json),
    )
    await db.commit()

    if cursor.lastrowid:
        logger.info(f"💾 Saved message {wa_message_id} from {from_number} (id={cursor.lastrowid})")
        return cursor.lastrowid
    else:
        logger.debug(f"⚠️ Duplicate message {wa_message_id} skipped")
        # Get existing row id
        row = await db.execute(
            "SELECT id FROM messages WHERE wa_message_id = ?", (wa_message_id,)
        )
        existing = await row.fetchone()
        return existing[0] if existing else 0


